package com.network.api.connection;

/**
 * Enum representing network protocols.
 */
public enum Protocol {
    /**
     * TCP protocol.
     */
    TCP,
    
    /**
     * UDP protocol.
     */
    UDP,
    
    /**
     * WebSocket protocol.
     */
    WEBSOCKET,
    
    /**
     * HTTP protocol.
     */
    HTTP,
    
    /**
     * HTTPS protocol.
     */
    HTTPS
}